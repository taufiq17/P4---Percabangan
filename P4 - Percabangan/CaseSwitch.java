public class CaseSwitch {
	public static void main(String[] args) {
		String color = "Blue";
		switch(color){
			case "Blue":
				System.out.println("My Favourite color is Blue");
				break;
			case "Red":
				System.out.println("My Favourite color is Red");
				break;
			case "Green":
				System.out.println("My Favourite color is Green");
				break;
			default:
				System.out.println("My Favourite color is Black");	
		}
	}
}